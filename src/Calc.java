    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        if(opr.equals("x^y")){
            jTextField2.setText(a+"^"+b+"="+res);
        }
        else if(opr.equals("n!")){
            jTextField2.setText(a+"!"+"="+res);
        }
        else if(opr.equals("x^2")){
            jTextField2.setText(a+"^"+"2"+"="+res);
        }
        else if(opr.equals("x^3")){
            jTextField2.setText(a+"^"+"3"+"="+res);
        }
        else if(opr.equals("+") || opr.equals("-") || opr.equals("*") || opr.equals("/") || opr.equals("%")){
            jTextField2.setText(a+opr+b+"="+res);
        }
        else if(opr.equals("exp")){
            jTextField2.setText("e^"+a+"="+res);
        }
        else{
            jTextField2.setText(opr+"("+a+")"+"="+res);
        }
        jTextField1.setText("");        // TODO add your handling code here:
    }//GEN-LAST:event_jButton19ActionPerformed

