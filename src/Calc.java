    private void jButton35ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton35ActionPerformed
        b=Double.parseDouble(jTextField1.getText());
        if(opr.equals("+")){
            res=a+b;
            jTextField1.setText(Double.toString(res));
        }
        else if(opr.equals("-")){
            res=a-b;
            jTextField1.setText(Double.toString(res));
        }
        else if(opr.equals("*")){
            res=a*b;
            jTextField1.setText(Double.toString(res));
        }
        else if(opr.equals("/")){
            res=a/b;
            jTextField1.setText(Double.toString(res));
        }
        else if(opr.equals("%")){
            res=a%b;
            jTextField1.setText(Double.toString(res));
        }
        else if(opr.equals("x^y")){
            res=Math.pow(a, b);
            jTextField1.setText(Double.toString(res));
        }
    }//GEN-LAST:event_jButton35ActionPerformed

